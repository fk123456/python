# 字符串转换为整形
num = 10
myStr = "20"
myStrInt = int(myStr)
print(num + myStrInt)

# 浮点型转换为整形(会舍弃调小数部分）
myFloat = 3.99
myFloatInt = int(myFloat)
print(myFloatInt)

# 整形转换为浮点型
myInt = 10
myIntFloat = float(myInt)
print(myIntFloat)

# 浮点型转换为字符串
myFloat1 = 10.10
myFloat1Str = str(myFloat1)
print(myFloat1Str)

